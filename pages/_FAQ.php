					<div class="title"><strong>Question n°1 :</strong> <em>Quel est le but de ce site internet ?</em></div>
					<div>
						<p>
							Ce site internet a pour but de rassembler tous les musées de France et de les classer par région, département, intérêt et par type. Il n'existe pas pour concurrencer les sites des musées eux-même. Grâce à ce site, plus besoin de chercher pendant de longue minute, un nom, une région, un département, un type et les réponses viendront simplement et rapidement.						
						</p>
					</div>
				</div>
				
				<div>
					<div class="title"><strong>Question n°2 :</strong> <em>Comment l'utilise t-on ?</em></div>
					<div>
						<p>
							Dès l'arrivée sur la page d'accueil, un champ texte apparaît, il vous suffit de rentrer le nom du musée, de valider et une liste apparaîtra.						</p>
						</p>
					</div>
				</div>
				
				<div>
					<div class="title"><strong>Question n°3 :</strong> <em>Si je souhaite inscrire un musée ?</em></div>
					<div>
						<p>
							Je dois envoyer un mail aux administrateurs en leur demandant d'ajouter un musée. Pour que ma demande soit prise en compte, je dois inclure dans le mail obligatoirement le nom du musée, son adresse internet, son département et sa région. Toute information supplémentaire sera la bien venue.
						</p>
