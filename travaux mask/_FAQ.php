			<h1>Foire Aux Questions</h1>
			
			<div id="question">
				<div>
					<div class="title"><strong>Question n°1 :</strong> <em>Quel est le but de ce site internet ?</em></div>
					<div>
						<p>
							Ce site internet a pour but de rassembler tous les musées de France et de les classer par région, département, intérêt et par type. Il n'existe pas pour concurrencer les sites des musées eux-même. Grâce à ce site, plus besoin de chercher pendant de longue minute, un nom, une région, un département, un type et les réponses viendront simplement et rapidement.						</p>
					</div>
				</div>
				
				<div>
					<div class="title"><strong>Question n°2 :</strong> <em>Comment l'utilise t-on ?</em></div>
					<div>
						<p>
							Dès l'arrivée sur la page d'accueil, un champ texte apparaît, il vous suffit de rentrer le nom du musée, de valider et une liste apparaîtra.						</p>
					</div>
				</div>
				
				<div>
					<div class="title"><strong>Question n°3 :</strong> <em>Si je souhaite inscrire un musée ?</em></div>
					<div>
						<p>
							Je dois envoyer un mail aux administrateurs en leur demandant d'ajouter un musée. Pour que ma demande soit prise en compte, je dois inclure dans le mail obligatoirement le nom du musée, son adresse internet, son département et sa région. Toute information supplémentaire sera la bien venue.
						</p>
					</div>
				</div>
				
				<div>
					<div class="title"><strong>Question n°4 :</strong> <em>Si ma recherche n'aboutit pas ?</em></div>
					<div>
						<p>
							Soit j'ai fait une erreur au niveau de l'écriture de ma demande, soit il n'existe aucune donnée en rapport avec ma demande.						
						</p>
					</div>
				</div>
				
				<div>
					<div class="title"><strong>Question n°5 :</strong> <em>Si je remarque une erreur pour un musée ?</em></div>
					<div>
						<p>
							Je dois informer les administrateurs en cliquant sur le lien Nous Contacter en haut à droite de la page (quatrième lien) et renseigner les champs demandés. Pour que la demande soit traitée le plus rapidement possible, je me dois d'inclure dans mon message le nom du musée, son adresse (ville et département au minimum) et son e-mail en plus de l'erreur que je dois mettre en évidence dans le message mais aussi dans le sujet du message (exemple de titre de message : erreur dans le nom du musée ou erreur dans les horaires du samedi). Les administrateurs vérifieront l'exactitude de la demande avant de corriger l'éventuelle erreur.					
						</p>
					</div>
				</div>
			</div>
			
