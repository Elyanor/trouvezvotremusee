			<h1>A Propos</h1>
			
			<h2 class="bleu">1. Nous sommes jeunes, ambitieux et indépendants !</h2>
			<p>
				L'idée de <em>Trouvez Votre Musée</em> nous est venu lors d'un trajet. Nous réfléchissions sur les concepts que nous pourrions développer sur internet. Nous voulions créer un site qui n'existait pas sur le marché d'Internet. <br />
				Quels étaient les sites qui existaient ? Qu'est-ce qui pouvait être amélioré ? Qu'est ce qui est réalisable ? Ces questions nous on conduit naturellement vers l'idée de créer un site qui permettrait de recenser tous les musées, de les classer et de permettre aux visiteurs d'y avoir un accès simple et rapide.			</p>
			
			<h2 class="text-droite vert">2. Simplicité et fonctionnalité.</h2>
			<p>
				Notre concept est très simple, mais « très fonctionnel ». Créé par notre équipe, celui-ci est fondé sur deux principes fondamentaux : la simplicité et la fonctionnalité. <em>Trouvez votre musée</em> est un projet intuitif et convivial, facile à utiliser tant par les professionnels du web et les débutants dans ce qui est la recherche des musées en France. Retrouvez ou découvrez un musée en deux clics seulement !
			</p>
			
			<h2 class="orange">3. Développement constant.</h2>
			<p>
				Nous sommes pleins d'idées sur la façon d'améliorer <em>Trouvez votre musée</em>. 
				Vous pouvez être sûr que nous n'allons pas nous arrêter en si bon chemin car nous croyons fermement que notre concept a un long chemin à vivre.
			</p>
